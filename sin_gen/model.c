double sin_gen(double x) {
	double ix, y, iix, iiix, z, appro_s, appro_c;
	long int temp, table_order, status_pi_4, status_pi_2, status_pi_1;
	int i, flag, sign, sin_or_cos;

	ix = x - X;
	temp = *((long int *)(&ix));
	flag = temp >> 63; // 负为1，正为0
	temp = temp & -1;
	ix = *((double *)(&temp)); // 此时 ix 为绝对值

	// ix过小，则return ix;
	if (temp < 0x3e40000000000000) {
		return (1 - flag * 2) * ix;
	}

	// new
	// iix = ix - [ix * invpio4] * pi_4
	// iiix = iix - i / 128* pi_4
	temp = (long int)(ix * invpio4 * BITNUM); // why not ix * invpio_512; ??
	table_order = temp & (BITNUM - 1); // 对应 (2^BIT -1) ，即BIT位的1
	status_pi_4 = (temp >> BIT) & 0x1; 
	status_pi_2 = (temp >> (BIT + 1)) & 0x1;
	status_pi_1 = (temp >> (BIT + 2)) & 0x1;
	iix = ix - (temp >> BIT) *pi_4 - status_pi_4 * pi_4;
	iiix = iix - (table_order / (2 << BIT) * pi_4);
	temp = *((long int *)(&iiix));
	temp = temp & -1;
	iiix = *((double *)(&temp)); // 此时 iix 为绝对值
	sign = flag ^ status_pi_1; // 异或，0为正，1为负
	sin_or_cos = status_pi_2 ^ status_pi_4; // 异或，0为sin，1为cos
	
	// new, sin(x+x') = sin(x)cos(x') + sin(x')cos(x), x' = iiix
	// OR, cos(x+x') = cos(x)cos(x') - sin(x)sin(x'), x' = iiix
	z = iiix * iiix;
	appro_s = coefficient[0][0] + coefficient[0][2] * z * (1 + coefficient[0][4] / coefficient[0][2] * z) + coefficient[0][1] * x * (1 + coefficient[0][3] / coefficient[0][1] * z * (1 + coefficient[0][5] / coefficient[0][3] * z));
	appro_c = coefficient[1][0] + coefficient[1][2] * z * (1 + coefficient[1][4] / coefficient[1][2] * z) + coefficient[1][1] * x * (1 + coefficient[1][3] / coefficient[1][1] * z * (1 + coefficient[1][5] / coefficient[1][3] * z));

	y = interpolate[sin_or_cos][table_order] * appro_c + (1 - sin_or_cos * 2) * interpolate[1 - sin_or_cos][table_order] * appro_s;
	return y;
}